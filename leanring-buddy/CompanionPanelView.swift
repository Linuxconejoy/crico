    private func memoryMenuRow<MenuContent: View>(
        label: String,
        detail: String,
        currentValue: String,
        iconName: String,
        @ViewBuilder menuContent: () -> MenuContent
    ) -> some View {
        HStack(alignment: .top, spacing: 12) {
            HStack(spacing: 8) {
                Image(systemName: iconName)
                    .font(.system(size: 12, weight: .medium))
